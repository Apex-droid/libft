#include "libft.h"

void	*ft_memmove(void *dest, const void *source, size_t n)
{
	unsigned char *d;
	unsigned char *s;

	if (dest == NULL && source == NULL)
		return (NULL);
	d = (unsigned char *)dest;
	s = (unsigned char *)source;
	if (d < s)
		while (n--)
			*d++ = *s++;
	else
	{
		d += n;
		s += n;
		while (n--)
			*--d = *--s;
	}
	return (dest);
}
